public class java {
    public static void main(String[] args) {
        int[] angka = {4, 8, 12, 16, 20};
        int total = 0;

        for (int i = 0; i < angka.length; i++) {
            total += angka[i];
        }
        System.out.println("Total jumlah elemen array = " + total);
    
    }
   
}
