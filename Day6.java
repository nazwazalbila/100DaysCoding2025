package com.mycompany.day6.java;


public class Day6Java {

    public static void main(String[] args) {
       //  Tipe Data Pecahan (double, float).
       // float harus pakai f diakhir angka desimalnya presisi 7 digit
       float angkakecil = 5.13f;
       //double angka desimal panjang dan tidak perlu pakai f
       double angkabesar = 5.1312976534532876;
               
        System.out.println("float :" + angkakecil);
        System.out.println("double:" + angkabesar);
    }
}
