package day25.javaa;

public class Day25Javaa {

        public static void main(String[] args) {
        
            //menggunakan bentuk prefix
            int a =8;
            int b = ++a;
            System.out.println(a);
            System.out.println(b);
            
            //menggunakan bentuk postfix
            int c = 10;
            int d = c--;
            System.out.println(c);
            System.out.println(d);
            
    }
    
}
