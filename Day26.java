package day26.java;

public class Day26Java {

    public static void main(String[] args) {
        
        int angka = 3;
         System.out.println("Nilai awal angka : " +angka);
         
        angka +=10;
         System.out.println("Nilai angka setelah += : " +angka);
         
        angka -=2;
         System.out.println("Nilai angka setelah -= : " +angka);

        angka *=9;
        System.out.println("Nilai angka setelah *=: " +angka);
        
        angka /=3;
         System.out.println("Nilai angka setelah /= : " +angka);
         
        angka %=8;
         System.out.println("Nilai angka setelah %= : " +angka);
       
       
               
       
       
    
    }
    
}
